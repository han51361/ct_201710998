def drawTriangleAt(size,pos):
    t1.penup()
    t1.goto(pos,0)
    t1.setheading(0)
    t1.pendown()
    t1.pos()
    t1.write(t1.pos())
    t1.right(60)
    t1.fd(size)
    t1.right(120)
    t1.fd(size)
    t1.right(120)
    t1.fd(size)
